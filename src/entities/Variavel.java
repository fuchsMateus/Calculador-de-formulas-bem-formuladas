package entities;

public class Variavel {

	private String nome;
	private int valor;
	
	public Variavel(String nome) {
		this.nome = nome;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public String getNome() {
		return nome;
	}
	
	
	
}
